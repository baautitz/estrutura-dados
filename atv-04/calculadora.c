#include "calculadora.h"

int soma(int valor1, int valor2) {
    return valor1 + valor2;
}

int subtracao(int valor1, int valor2) {
    return valor1 - valor2;
}

int multiplicacao(int valor1, int valor2) {
    return valor1 * valor2;
}

float divisao(int valor1, int valor2) {
    return valor1 / valor2;
}